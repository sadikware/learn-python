

# Variables Naming Convention

# Variable name always start letter

age = 23

# variable name start underscore

_name = 'Sadik Rahman'

# Variable name can not start a number or any symbol

# Variable name Case Sensitive

# Those are  different Variables
price = 12
Price = 17

# Variale name can not use any reserved Keyword

# snake case ---- first_name
# camel case ---- firstName
# pascal case --- FirstName
